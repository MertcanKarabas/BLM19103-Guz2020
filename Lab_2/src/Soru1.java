class Soru1{
	public static void main(String[] args){
		int a = 5;
		int b = 7;

		//bitwise (bit düzeyinde) and operatörü
		System.out.println("a&b = " + (a & b));

		//bitwise (bit düzeyinde) or operatörü
		System.out.println("a|b = " + (a | b));

		//bitwise (bit düzeyinde) xor operatörü
		System.out.println("a^b = " + (a ^ b));

		//bitwise 2'nin tamlayanı
		System.out.println("~a= " + ~a);


	}
}